/**
 * Stupid Helper Class
 * 
 * @author aw
 */
public class NullCheck {
	
	/**
	 * If obj is null print the statck and throw a {@link NullPointerException}
	 * with message msg.
	 */
	public static void check(Object obj, String msg) {
		if (obj == null) {
			throw new NullPointerException(msg);
		}
	}
}
